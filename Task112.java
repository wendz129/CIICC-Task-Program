public class Task112 {

    public static void main(String[] args){

        int a = 10;  
        int b = 5;  

        // Addition  
        int sum = a + b;  
        System.out.println("Sum: " + sum); // Output: Sum: 15  

        // Subtraction  
        int difference = a - b;  
        System.out.println("Difference: " + difference); // Output: Difference: 5  

        // Multiplication  
        int product = a * b;  
        System.out.println("Product: " + product); // Output: Product: 50  

        // Division  
        double quotient = (double) a / b; // Casting to double for accurate division  
        System.out.println("Quotient: " + quotient); // Output: Quotient: 2.0  

        // Modulus  
        int remainder = a % b;  
        System.out.println("Remainder: " + remainder); // Output: Remainder: 0  
    }

}